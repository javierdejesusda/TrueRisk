async def seed_provinces() -> None:
    """Insert all 52 provinces if the table is empty."""
    async with async_session() as session:
        result = await session.execute(select(Province).limit(1))
        if result.scalars().first() is not None:
            return

        for ine_code, data in PROVINCES.items():
            province = Province(
                ine_code=ine_code,
                name=data["name"],
                capital_name=data["capital_name"],
                capital_municipality_code="",
                region=data["region"],
                latitude=data["latitude"],
                longitude=data["longitude"],
                elevation_m=data["elevation_m"],
                river_basin=data["river_basin"],
                coastal=data["coastal"],
                mediterranean=data["mediterranean"],
                flood_risk_weight=data["flood_risk_weight"],
                wildfire_risk_weight=data["wildfire_risk_weight"],
                drought_risk_weight=data["drought_risk_weight"],
                heatwave_risk_weight=data["heatwave_risk_weight"],
            )
            session.add(province)

        await session.commit()
